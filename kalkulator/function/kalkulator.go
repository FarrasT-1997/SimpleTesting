package function

func Addition(number1, number2 int) int {
	result := number1 + number2
	return result
}

func Substraction(number1, number2 int) int {
	result := number1 - number2
	return result
}

func Division(number1, number2 float32) float32 {
	var result float32
	result = number1 / number2
	return result
}

func Multiplication(number1, number2 int) int {
	result := number1 * number2
	return result
}
